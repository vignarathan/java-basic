class Nested6{
	public static void main(String args[]){
	int x=1;
	while(x<=5){
		int y=1;
		while(y<=x){
			 System.out.print(y);
			 y++;
		}
	    System.out.println(" ");
	    x++;
	}
	System.out.println("-----------------");
	int a=1;
	while(a<=5){
		int b=1;
		while(b<=a){
			 System.out.print(a);
			 b++;
		}
	    System.out.println(" ");
	    a++;
	}
	
			
	}
}