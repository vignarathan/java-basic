class ForLoop2{
	public static void main(String args[]){
		for(int x=1;x<=5;x++){
			for(int y=1;y<=5;y++){
			System.out.print("*");
			}
			System.out.println("");
		}
	  System.out.println("----------------");
	 
	    for(int a=1;a<=5;a++){
			for(int b=1;b<=5;b++){
			System.out.print(a);
			}
			System.out.println("");
		}
	  System.out.println("----------------");
	   
	    for(int c=5;c>0;c=c-1){
			for(int d=1;d<=5;d++){
				System.out.print(c);
			}
			System.out.println("");
		}
	}
}